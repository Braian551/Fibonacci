# ============================================
# CONVERTIR MAYÚSCULAS Y MINÚSCULAS
# ============================================
# Este programa convierte una palabra a mayúsculas y minúsculas
# ============================================

# Pedir al usuario que ingrese una palabra
palabra = input("Ingrese una palabra: ")

# Convertir la palabra a MAYÚSCULAS
palabraMayuscula = palabra.upper()
print(palabraMayuscula)

# Convertir la palabra a minúsculas
palabraMinuscula = palabra.lower()
print(palabraMinuscula)

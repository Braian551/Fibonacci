#Programa que convierte grados celsius en fahrenheit
print("#Programa que convierte grados celsius en fahrenheit")

grad = float(input("Ingresa los grados celsius a convertir: ")) #El input para ingresa los grados a convertir, tipo float

fahren = (1.8 * grad) + 32 #La formula de conversion

print (f"{grad}° celsius son {fahren}° fahrenheit") #Imprimo el resultado